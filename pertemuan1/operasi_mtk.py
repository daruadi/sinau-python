# + - * / % < > == <= >=
angka1 = 10
angka2 = 20
angka_a = 100
angka_b = 30
angka_ganjil = 5
angka_genap = 2
print(angka1 + angka2) # 30
print(angka1 - angka2) # -10
print(angka1 * angka2) # 200
print(angka2 / angka1) # 2 -> float
print(angka_a / angka_b) # 2 -> float
print(angka_a // angka_b) # 3 -> dibagi terus pembulatan ke bawah integer
print(angka1 ** angka2) # ----
print(angka_ganjil % angka_genap) # 5 % 2 = 1
print(angka1 < angka2) # True
print(angka1 > angka2) # False
print((angka1 + angka2) == angka_b) # True
print((angka1 + angka2) >= angka_b) # True